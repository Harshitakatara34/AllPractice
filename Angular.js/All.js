// What is AngularJS?
// AngularJS is an open-source JavaScript framework developed by Google for building dynamic web applications.
//  It allows you to use HTML as your template language and extend the HTML's syntax to express the components of
//  your application.

// What are the key features of AngularJS?
// Key features of AngularJS include two-way data binding, templates, MVC (Model-View-Controller) architecture, directives, filters, form validation, dependency injection, and testing.

// What is data binding in AngularJS?
// Data binding is an automatic way of updating the view whenever the model changes, and updating the model whenever the view changes. In AngularJS, there are two types of data binding: one-way binding and two-way binding.

// Explain directives in AngularJS.
// Directives are markers on DOM elements that tell AngularJS's HTML compiler to attach a specified behavior to that DOM element or even transform the DOM element and its children. Examples include ng-app, ng-model, ng-bind, ng-repeat, and more.

// What is scope in AngularJS?
// Scope is an object that refers to the application model. It acts as a context for evaluating AngularJS expressions. Scopes are arranged in hierarchical structure which mimic the DOM structure of the application.

// Explain services in AngularJS.
// Services are objects that are used to organize and share code across the application. AngularJS has a set of built-in services such as $http for making XMLHttpRequests, $route for routing, $location for URL manipulation, and more.

// What is routing in AngularJS?
// Routing is the process of switching views. It allows you to define the application states and then map those states to specific URLs. It helps to divide your application into different views or sections without loading the entire page.

// What is the difference between $scope and scope in AngularJS?
// In AngularJS, $scope is the built-in object, while scope is a parameter that is used to pass the scope to the linking function. $scope is used in controllers, while scope is used in directives.

// Explain dependency injection in AngularJS.
// Dependency injection is a design pattern in which an object receives other objects that it depends on. AngularJS has a built-in dependency injection subsystem that helps the developer to create, maintain, and test the applications more efficiently.

// How do you perform form validation in AngularJS?
// AngularJS provides form validation and validity checks using the ngModel controller and a set of built-in validation directives such as ng-required, ng-minlength, ng-maxlength, ng-pattern, and more.
