// - useContext and Redux are two different approaches for managing the state of a React application.



// useContext is a hook that provides a way to share data between components without passing props 
// down the component tree manually. It's a simpler approach compared to Redux and is often used for 
// sharing global data that doesn't need to be updated frequently, such as themes or translations.







// Redux, on the other hand, is a state management library that provides a centralized store for the 
// entire application state and a way to update the state with actions and reducers. It's often used 
// for more complex state management where the state needs to be updated frequently and in a predictable way, 
// such as in a data-driven application.