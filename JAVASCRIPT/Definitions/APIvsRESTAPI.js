// API vs. REST API (Short):

// API: General term for communication protocols.(DOM)
// REST API: Specific type following REST principles.
// API: Various protocols, flexible design.
// REST API: Standardized with HTTP methods, URLs.
// API: Broad, less convention.
// REST API: Specific, follows conventions for resource access.

// Feature	     API	                                REST API
// Scope	General term (any interface)	    Specific type of web API using HTTP
// Protocol	Can be anything              	    Always uses HTTP/HTTPS
// Format	Any format	                        Usually JSON / XML
// Structure	Unstructured	                Follows REST rules (verbs, URIs, stateless)